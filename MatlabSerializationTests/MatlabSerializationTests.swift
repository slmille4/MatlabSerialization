            guard let data = buffer.mData?.assumingMemoryBound(to: Float32.self) else { continue }
            
            for i in 0..<outBuffer.frameCapacity {
                //        data[i] = counter
                data.advanced(by: Int(i)).pointee = counter
                counter += 1
            }
